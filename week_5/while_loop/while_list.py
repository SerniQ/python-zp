# Sumowanie liczb
numbers = [1, 3, 510, 123, 24]
numbers_sum = 0
number_index = 0
while number_index < len(numbers):
    numbers_sum += numbers[number_index]
    number_index += 1
print(f"Suma: {numbers_sum}")

